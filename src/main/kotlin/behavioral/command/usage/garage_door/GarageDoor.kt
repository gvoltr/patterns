package behavioral.command.usage.garage_door

class GarageDoor {

    fun up() {
        println("Garage door up")
    }

    fun down() {
        println("Garage door down")
    }

    fun stop() {
        println("Garage door stop")
    }

    fun lightOn() {
        println("Garage light on")
    }

    fun lightOff() {
        println("Garage light off")
    }

}